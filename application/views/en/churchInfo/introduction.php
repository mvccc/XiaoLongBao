	<h2>Our History</h2>
	<p><i>
	Started in the 1960s as a Home Bible Study Group under the guidance and assistance of Rev. Moses Yu, Chinese Christian Fellowship held its first
	worship service in 1964 in the hall rented from Masonic Temple. In 1974, it formally changed its name to Mountain View Chinese Christian Church,
	and on December 8, 1996, MVCCC moved into its current facility.
